// Program to determine if a < b

#include <iostream>
using namespace std;

int main()
{
    int a,b;

    cout<<"Enter value of a :";
    cin>>a;

    cout<<"Enter value of b :";
    cin>>b;

    if(a<b)
    {
        cout<<"True";
    }
    else
    {
        cout<<"False";
    }

    return 0;
}
