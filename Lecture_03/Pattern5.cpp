// A B C D E 
// B C D E 
// C D E 
// D E 
// E

#include <iostream>
using namespace std;

int main()
{
    int n;
    cout << "Enter number of rows: ";
    cin >> n;

    for (int i = 0; i < n; i++)  
    {
        for (int j = i; j < n; j++)   
        {
            cout << char('A' + j) << " ";
        }
        cout << endl;
    }

    return 0;
}
